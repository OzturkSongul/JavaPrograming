package Day18_NestedLoop;

public class NestedLoopPractice3 {
    public static void main(String[] args) {
        for (int j = 0; j < 10; j++) {//to repeat 10 times in new line//0,1 each time write 10 times star


            for (int i = 0; i < 10; i++) {//responsibility of this loop write 10 times star
                System.out.print("* ");
            }
            System.out.println();//to print a new line

        }
    }
}

