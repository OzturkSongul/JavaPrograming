public class WhileLoopIntro {
    public static void main(String[] args) {

        if (true){
            System.out.println("wooden spoon");//1 time

            System.out.println(".............");
            while (true){//repeated if
                System.out.println("wooden spoon");//infinitive time
            }


        }
    }
}
